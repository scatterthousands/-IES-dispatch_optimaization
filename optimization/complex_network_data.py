# ==========================================
# 8. 时间序列数据生成函数
# ==========================================
def get_time_series_data():
    T = SIM_SETTINGS['T']
    t = np.arange(T)

    # 室外温度
    T_amb = -10 + 5 * np.sin((t - 9) * np.pi / 12)

    # 电价
    Price_Elec = np.ones(T) * 0.15
    Price_Elec[0:7] = 0.02
    Price_Elec[18:21] = 0.30

    # 风电
    Wind_Power = 1200 + 800 * np.cos((t - 3) * np.pi / 12)
    Wind_Power = np.maximum(Wind_Power, 0)

    # 电负荷
    Elec_Load = 1200 + 400 * np.exp(-0.1 * (t - 10) ** 2) + 500 * np.exp(-0.1 * (t - 19) ** 2)

    return {
        'T_amb': T_amb,
        'Price_Elec': Price_Elec,
        'Wind_Power': Wind_Power,
        'Elec_Load': Elec_Load
    }